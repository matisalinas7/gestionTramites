package CambioEstado.dtos;

public class DTOEstadoDestino {
    private int codEstadoDestino;
    private String nombreEstadoDestino;

    // Getters y Setters

    public int getCodEstadoDestino() {
        return codEstadoDestino;
    }

    public void setCodEstadoDestino(int codEstadoDestino) {
        this.codEstadoDestino = codEstadoDestino;
    }

    public String getNombreEstadoDestino() {
        return nombreEstadoDestino;
    }

    public void setNombreEstadoDestino(String nombreEstadoDestino) {
        this.nombreEstadoDestino = nombreEstadoDestino;
    }
}
